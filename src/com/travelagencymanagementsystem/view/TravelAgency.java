    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
    String name = TxtFldSerachField.getText().trim();
    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the Csutomer name to search.");
        return;
    }

    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>((DefaultTableModel) CustomerTable.getModel());
    CustomerTable.setRowSorter(sorter);

    sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + name + "$", 1)); // Match name in column 1 (case-insensitive)

    if (CustomerTable.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Customer not found.");
        sorter.setRowFilter(null); // Reset filter if no match is found
    }   
    }//GEN-LAST:event_btnSearchActionPerformed

