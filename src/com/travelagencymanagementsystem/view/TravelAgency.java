    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
         String[] options = {"Sort by Customer Name (Insertion Sort)", 
                        "Sort by Passport Number (Selection Sort)", 
                        "Sort by Travel Date (Merge Sort)"};
    
    int choice = JOptionPane.showOptionDialog(this, "Choose a sorting method:",
            "Sort Options", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
            null, options, options[0]);

    if (choice == JOptionPane.CLOSED_OPTION) {
        return; // User closed the dialog
    }

    // Sort the customer list based on the user's choice
    switch (choice) {
        case 0: // Insertion Sort by Customer Name
            InsertionSort.sortByCustomerName(customerList, true); // Sort in ascending order
            break;
        case 1: // Selection Sort by Passport Number
            SelectionSort.sortByPassportNumber(customerList, true); // Sort in ascending order
            break;
        case 2: // Merge Sort by Travel Date
            MergeSort.sortByTravelDate(customerList, true); // Sort in ascending order
            break;
    }

    // After sorting, reload the customer list to the table
    loadCustomerListToTable();
    JOptionPane.showMessageDialog(this, "Data sorted successfully using " + options[choice]);

    }//GEN-LAST:event_btnSortActionPerformed

