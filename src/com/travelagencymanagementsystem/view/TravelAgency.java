    public void startProgress() {
        javax.swing.SwingWorker<Void, Integer> worker = new javax.swing.SwingWorker<>() {
            @Override
            protected Void doInBackground() throws Exception {
                for (int i = 0; i <= 100; i++) {
                    Thread.sleep(20); // Simulated delay for progress bar
                    publish(i); // Publish progress
                }
                return null;
            }

             @Override
            protected void process(java.util.List<Integer> chunks) {
                int progress = chunks.get(chunks.size() - 1);
                ProgressbarLoader.setValue(progress);
                ProgressbarLoad.setText(progress + "%"); // Update label text to show progress
            }

              @Override
            protected void done() {
                // Switch to login screen after loading is complete
                cardLayout.show(getContentPane(), "LoginScreen");
            }
        };
        worker.execute(); // Start the worker thread
    }
    
